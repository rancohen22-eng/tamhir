CREATE OR REPLACE PACKAGE booking_pkg AS

  g_mail_from CONSTANT VARCHAR2(200) := 'no-reply@arkia.example';  -- לשנות לכתובת השולח בפועל

  -- ── אימות / סיסמאות ──
