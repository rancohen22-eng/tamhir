-- מיועד ל-Oracle Autonomous Database + APEX.
-- הרצה: SQLcl / SQL Workshop בסכמת האפליקציה.
-- הסקריפט idempotent: מוחק טבלאות קיימות (אם יש) ובונה מחדש.
--------------------------------------------------------------------------------

-- ── ניקוי (מתעלם משגיאת "טבלה לא קיימת" ORA-00942) ──
BEGIN
  FOR t IN (
    SELECT table_name FROM user_tables
    WHERE table_name IN (
      'BOOKING_STATUS_LOG','BOOKING_FILES','NOTIFICATIONS','REPORT_TEMPLATES',
      'BOOKINGS','DEPT_APPROVERS','USER_ROLES','AGENTS','APP_USERS',
      'DEPARTMENTS','CURRENCIES','ROLES','STATUSES','NOTIF_TEMPLATES'
    )
  ) LOOP
    EXECUTE IMMEDIATE 'DROP TABLE "'||t.table_name||'" CASCADE CONSTRAINTS PURGE';
  END LOOP;
END;
