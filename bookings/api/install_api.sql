  -- מחיקת משתמש. חסום אם למשתמש יש הזמנות מקושרות (FK) — מחזיר שגיאה ברורה.
  FUNCTION admin_delete_user (p_token VARCHAR2, p_user_id NUMBER) RETURN CLOB IS
    l_uid NUMBER := admin_guard(p_token);
    n PLS_INTEGER;
  BEGIN
    IF p_user_id = l_uid THEN RETURN err('cannot_delete_self'); END IF;
    SELECT COUNT(*) INTO n FROM bookings
      WHERE initiator_id = p_user_id OR approver_id = p_user_id
         OR cancel_requested_by = p_user_id
         OR agent_id IN (SELECT agent_id FROM agents WHERE user_id = p_user_id);
    IF n > 0 THEN RETURN err('user_has_bookings'); END IF;
    DELETE FROM dept_approvers WHERE approver_user_id = p_user_id;
    DELETE FROM agents         WHERE user_id = p_user_id;
    DELETE FROM user_roles     WHERE user_id = p_user_id;   -- FK cascade ממילא
    DELETE FROM app_users      WHERE user_id = p_user_id;
    IF SQL%ROWCOUNT = 0 THEN ROLLBACK; RETURN err('user_not_found'); END IF;
    COMMIT; RETURN '{"ok":true}';
  EXCEPTION WHEN OTHERS THEN ROLLBACK; RETURN err(SQLERRM);
  END admin_delete_user;

  -- לוג כניסות (100 האחרונות)
  FUNCTION admin_logins (p_token VARCHAR2) RETURN CLOB IS
    l_uid NUMBER := admin_guard(p_token);
  BEGIN
    APEX_JSON.initialize_clob_output;
    APEX_JSON.open_object; APEX_JSON.write('ok', TRUE); APEX_JSON.open_array('logins');
    FOR r IN (SELECT l.username, u.full_name,
                     TO_CHAR(l.login_at,'YYYY-MM-DD HH24:MI:SS') atx
                FROM login_log l LEFT JOIN app_users u ON u.user_id = l.user_id
               ORDER BY l.login_at DESC FETCH FIRST 100 ROWS ONLY) LOOP
      APEX_JSON.open_object; APEX_JSON.write('username', r.username);
      APEX_JSON.write('full_name', r.full_name); APEX_JSON.write('at', r.atx); APEX_JSON.close_object;
    END LOOP;
    APEX_JSON.close_array; APEX_JSON.close_object;
    RETURN APEX_JSON.get_clob_output;
  EXCEPTION WHEN OTHERS THEN APEX_JSON.free_output; RETURN err(SQLERRM);
  END admin_logins;

