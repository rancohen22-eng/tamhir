    l_uid NUMBER := uid(p_token); l_dept NUMBER; l_id NUMBER;
  BEGIN
    SELECT dept_id INTO l_dept FROM app_users WHERE user_id = l_uid;
    -- מודל בריכה: אין סוכן ספציפי בייזום — ההזמנה נפתחת לכל הסוכנים.
