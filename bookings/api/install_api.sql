  -- התראה לכל הסוכנים הפעילים (מודל "בריכה" — כל סוכן יכול להגיב) — פעמון + מייל
  PROCEDURE notify_agents (p_id NUMBER) IS
  BEGIN
    FOR a IN (SELECT ag.agent_id, u.user_id, u.email, u.pref_lang, NVL(u.notify_email,'Y') notify
                FROM agents ag JOIN app_users u ON u.user_id = ag.user_id
               WHERE ag.is_active='Y' AND u.is_active='Y') LOOP
      INSERT INTO notifications (user_id, booking_id, notif_type, message)
      VALUES (a.user_id, p_id, 'AWAITING_QUOTE',
              CASE WHEN a.pref_lang='EN' THEN 'New quote request #'||p_id
                   ELSE 'בקשה חדשה להצעת מחיר #'||p_id END);
      IF a.email IS NOT NULL AND a.notify = 'Y' THEN
        BEGIN
          BEGIN APEX_UTIL.SET_SECURITY_GROUP_ID(APEX_UTIL.FIND_SECURITY_GROUP_ID('ARKIA')); EXCEPTION WHEN OTHERS THEN NULL; END;
          APEX_MAIL.SEND(p_to => a.email, p_from => booking_pkg.g_mail_from,
            p_subj => CASE WHEN a.pref_lang='EN' THEN 'Arkia — new quote request #'||p_id ELSE 'ארקיע — בקשה חדשה להצעת מחיר #'||p_id END,
            p_body => TO_CLOB((CASE WHEN a.pref_lang='EN' THEN 'A new booking is awaiting your quote. Sign in to respond.' ELSE 'התקבלה בקשה חדשה הממתינה להצעת מחיר. היכנסו למערכת כדי להגיב.' END)
                      || CHR(10) || booking_pkg.g_app_url || '#b=' || p_id),
            p_body_html => booking_pkg.booking_email_html(p_id, a.pref_lang));
        EXCEPTION WHEN OTHERS THEN NULL; END;
      END IF;
    END LOOP;
    BEGIN APEX_MAIL.PUSH_QUEUE; EXCEPTION WHEN OTHERS THEN NULL; END;
  END notify_agents;

  -- מייל לכל מאשרי המחלקה עם כפתור "אשר את ההצעה" (טוקן חד-פעמי לכל מאשר) — נשלח כשהוגשה הצעת מחיר
  PROCEDURE notify_approver_quote (p_id NUMBER) IS
    l_dept NUMBER; l_pinned NUMBER; l_any BOOLEAN := FALSE;
    l_token VARCHAR2(70); l_mail_id NUMBER; l_url VARCHAR2(400);
  BEGIN
    SELECT dept_id, approver_id INTO l_dept, l_pinned FROM bookings WHERE booking_id = p_id;
    -- נמעני האישור: כל המאשרים המוגדרים של המחלקה + המאשר המשויך (אם קיים), ללא כפילויות
    FOR a IN (
        SELECT u.user_id, u.email, u.pref_lang, NVL(u.notify_email,'Y') notify
          FROM app_users u
         WHERE u.user_id IN (
                 SELECT approver_user_id FROM dept_approvers WHERE dept_id = l_dept
                 UNION
                 SELECT l_pinned FROM dual WHERE l_pinned IS NOT NULL)
    ) LOOP
      l_any := TRUE;
      l_token := RAWTOHEX(SYS_GUID()) || RAWTOHEX(SYS_GUID());
      INSERT INTO booking_action_tokens (token, booking_id, action, user_id, expires_at)
        VALUES (l_token, p_id, 'approve', a.user_id, SYSTIMESTAMP + INTERVAL '7' DAY);
      INSERT INTO notifications (user_id, booking_id, notif_type, message)
        VALUES (a.user_id, p_id, 'QUOTE_RECEIVED',
          CASE WHEN a.pref_lang='EN' THEN 'A quote was submitted for booking #'||p_id||' — approval required'
               ELSE 'התקבלה הצעת מחיר להזמנה #'||p_id||' — נדרש אישור' END);
      IF a.email IS NOT NULL AND a.notify = 'Y' THEN
        l_url := REPLACE(booking_pkg.g_app_url, '/app', '') || '/act?t=' || l_token;
        BEGIN
          BEGIN APEX_UTIL.SET_SECURITY_GROUP_ID(APEX_UTIL.FIND_SECURITY_GROUP_ID('ARKIA')); EXCEPTION WHEN OTHERS THEN NULL; END;
          l_mail_id := APEX_MAIL.SEND(p_to => a.email, p_from => booking_pkg.g_mail_from,
            p_subj => CASE WHEN a.pref_lang='EN' THEN 'Arkia — approval required, booking #'||p_id ELSE 'ארקיע — נדרש אישור, הזמנה #'||p_id END,
            p_body => TO_CLOB((CASE WHEN a.pref_lang='EN' THEN 'A quote was submitted and requires your approval.' ELSE 'התקבלה הצעת מחיר הממתינה לאישורך.' END)
                      || CHR(10) || 'Approve: ' || l_url || CHR(10) || booking_pkg.g_app_url || '#b=' || p_id),
            p_body_html => booking_pkg.booking_email_html(p_id, a.pref_lang, l_url));
          FOR f IN (SELECT filename, mime_type, file_blob FROM booking_files WHERE booking_id = p_id) LOOP
            BEGIN APEX_MAIL.ADD_ATTACHMENT(p_mail_id => l_mail_id, p_attachment => f.file_blob,
              p_filename => f.filename, p_mime_type => NVL(f.mime_type,'application/octet-stream')); EXCEPTION WHEN OTHERS THEN NULL; END;
          END LOOP;
        EXCEPTION WHEN OTHERS THEN NULL; END;
      END IF;
    END LOOP;
    COMMIT;
    IF l_any THEN BEGIN APEX_MAIL.PUSH_QUEUE; EXCEPTION WHEN OTHERS THEN NULL; END; END IF;
  EXCEPTION WHEN OTHERS THEN NULL;   -- לא מפיל את הגשת ההצעה
  END notify_approver_quote;

  -- אישור/פעולה מתוך המייל (טוקן חד-פעמי). autonomous — מבצע DML ומחזיר עמוד HTML.
  FUNCTION act_token (p_t VARCHAR2) RETURN BLOB IS
    PRAGMA AUTONOMOUS_TRANSACTION;
    l_bid NUMBER; l_uid NUMBER; l_action VARCHAR2(20); l_used CHAR(1);
    FUNCTION page (p_title VARCHAR2, p_msg VARCHAR2, p_bid NUMBER) RETURN BLOB IS
      l VARCHAR2(4000);
    BEGIN
      l := '<!doctype html><html dir="rtl" lang="he"><head><meta charset="utf-8">'||
           '<meta name="viewport" content="width=device-width,initial-scale=1"><title>'||p_title||'</title></head>'||
           '<body style="font-family:Heebo,Arial,sans-serif;background:#f2f5f9;margin:0;padding:24px">'||
           '<div style="max-width:460px;margin:40px auto;background:#fff;border-radius:14px;border:1px solid #e2e8f0;overflow:hidden">'||
           '<div style="background:#123a86;padding:16px;text-align:center">'||
           '<img src="'||booking_pkg.g_assets_url||'arkia" height="26" style="background:#fff;border-radius:6px;padding:3px 7px;vertical-align:middle">'||
           '<span style="color:#fff;margin:0 10px">&#10005;</span>'||
           '<img src="'||booking_pkg.g_assets_url||'teltos" height="26" style="background:#fff;border-radius:6px;padding:3px 7px;vertical-align:middle"></div>'||
           '<div style="padding:22px;text-align:center">'||
           '<h2 style="color:#123a86;margin:0 0 8px">'||p_title||'</h2>'||
           '<p style="color:#5b6b7f">'||p_msg||'</p>'||
           CASE WHEN p_bid IS NOT NULL THEN '<a href="'||booking_pkg.g_app_url||'#b='||p_bid||'" style="display:inline-block;margin-top:10px;background:#0e7a4e;color:#fff;text-decoration:none;padding:11px 22px;border-radius:9px;font-weight:700">צפייה בהזמנה</a>' ELSE '' END||
           '</div></div></body></html>';
      RETURN TO_BLOB(UTL_I18N.STRING_TO_RAW(l, 'AL32UTF8'));
    END page;
  BEGIN
    BEGIN
      SELECT booking_id, user_id, action, used INTO l_bid, l_uid, l_action, l_used
        FROM booking_action_tokens WHERE token = p_t AND expires_at > SYSTIMESTAMP;
    EXCEPTION WHEN NO_DATA_FOUND THEN
      COMMIT; RETURN page('קישור לא תקין', 'הקישור אינו תקף או שפג תוקפו.', NULL);
    END;
    IF l_used = 'Y' THEN
      COMMIT; RETURN page('כבר טופל', 'הפעולה כבר בוצעה עבור הזמנה זו.', l_bid);
    END IF;
    IF l_action = 'approve' THEN
      booking_pkg.approve(l_bid, l_uid, 'אושר מתוך המייל');
    END IF;
    UPDATE booking_action_tokens SET used = 'Y' WHERE token = p_t;
    COMMIT;
    RETURN page('ההצעה אושרה ✓', 'ההזמנה אושרה בהצלחה. הסוכן יכול להמשיך לכרטוס.', l_bid);
  EXCEPTION WHEN OTHERS THEN
    ROLLBACK; RETURN page('שגיאה', 'אירעה שגיאה בעיבוד הבקשה.', NULL);
  END act_token;

  FUNCTION create_booking (p_token VARCHAR2, p_departure VARCHAR2, p_notes VARCHAR2,
