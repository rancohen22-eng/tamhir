-- מיגרציית סכמה (idempotent) — נוספת אוטומטית בכל פריסת CI.
-- הערה: bookings/db/** אינו נפרס אוטומטית, ולכן שינויי הסכמה הנדרשים לשיפורים
--       (שדות חדשים בהזמנה, טבלאות מפעיל-רטוב ושדות-תעופה) מבוצעים כאן, בבטחה,
--       בלי למחוק נתונים קיימים.
