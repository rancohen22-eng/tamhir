DECLARE
  PROCEDURE try (p_ddl VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE p_ddl;
  EXCEPTION WHEN OTHERS THEN NULL;  -- עמודה/טבלה כבר קיימת → התעלם (idempotent)
  END;
BEGIN
  -- שדות חדשים בהזמנה
  try('ALTER TABLE bookings ADD (pax_count NUMBER DEFAULT 1)');
  try('ALTER TABLE bookings ADD (pref_time VARCHAR2(20))');
  try('ALTER TABLE bookings ADD (wet_operator_id NUMBER)');
  try('ALTER TABLE bookings ADD (origin_iata VARCHAR2(4))');
  try('ALTER TABLE bookings ADD (dest_iata VARCHAR2(4))');
  try('ALTER TABLE bookings ADD (low_cost CHAR(1) DEFAULT ''N'')');   -- טיסת LOW COST (הסוכן מסמן בהצעה)
  try('ALTER TABLE bookings ADD (urgent CHAR(1) DEFAULT ''N'')');     -- הצעה דחופה יזומה ע"י הסוכן
  -- העדפת התראות מייל לכל משתמש (Y=מקבל, N=כבוי)
  try('ALTER TABLE app_users ADD (notify_email CHAR(1) DEFAULT ''Y'')');
  -- לוג כניסות למערכת (התחברויות בלבד — לא פעולות)
  try('CREATE TABLE login_log (
        log_id   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        user_id  NUMBER,
        username VARCHAR2(60),
        login_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL)');
  -- טוקני פעולה חד-פעמיים (למשל אישור הצעה מתוך המייל, ללא כניסה למערכת)
  try('CREATE TABLE booking_action_tokens (
        token      VARCHAR2(70) PRIMARY KEY,
        booking_id NUMBER NOT NULL,
        action     VARCHAR2(20) NOT NULL,
        user_id    NUMBER,
        created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        expires_at TIMESTAMP,
        used       CHAR(1) DEFAULT ''N'' NOT NULL)');
  -- טבלת מפעיל רטוב (ACMI) — מנוהלת ע"י אדמין
  try('CREATE TABLE wet_operators (
        operator_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        name_he     VARCHAR2(80) NOT NULL,
        name_en     VARCHAR2(80),
        is_active   CHAR(1) DEFAULT ''Y'' NOT NULL)');
  -- טבלת שדות תעופה (נתיב טיסה לפי IATA) — מנוהלת ע"י אדמין
  try('CREATE TABLE airports (
        iata      VARCHAR2(4) PRIMARY KEY,
        name_he   VARCHAR2(120),
        name_en   VARCHAR2(120),
        city_he   VARCHAR2(80),
        city_en   VARCHAR2(80),
        country   VARCHAR2(60),
        is_active CHAR(1) DEFAULT ''Y'' NOT NULL)');
  -- הרחבת עמודות שדות התעופה (שמות אנגליים ארוכים ממערכת התמחיר)
  try('ALTER TABLE airports MODIFY (name_he VARCHAR2(200))');
  try('ALTER TABLE airports MODIFY (name_en VARCHAR2(200))');
  try('ALTER TABLE airports MODIFY (city_he VARCHAR2(200))');
  try('ALTER TABLE airports MODIFY (city_en VARCHAR2(200))');
  try('ALTER TABLE airports MODIFY (country VARCHAR2(100))');
END;
