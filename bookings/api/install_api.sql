  FUNCTION admin_save_airport (p_token VARCHAR2, p_iata VARCHAR2, p_name_he VARCHAR2, p_name_en VARCHAR2,
                               p_country VARCHAR2, p_is_active VARCHAR2) RETURN CLOB IS
    l_uid  NUMBER := admin_guard(p_token);
    l_iata VARCHAR2(4) := UPPER(TRIM(p_iata));
  BEGIN
    IF l_iata IS NULL THEN RETURN err('iata_required'); END IF;
    MERGE INTO airports a USING (SELECT l_iata AS iata FROM dual) s ON (a.iata = s.iata)
    WHEN MATCHED THEN UPDATE SET
      name_he   = NVL(p_name_he, a.name_he),
      name_en   = NVL(p_name_en, a.name_en),
      country   = NVL(p_country, a.country),
      is_active = NVL(p_is_active, a.is_active)
    WHEN NOT MATCHED THEN INSERT (iata, name_he, name_en, city_he, city_en, country, is_active)
      VALUES (l_iata, p_name_he, NVL(p_name_en, p_name_he), p_name_he, NVL(p_name_en, p_name_he),
              p_country, NVL(p_is_active,'Y'));
    COMMIT; RETURN '{"ok":true}';
  EXCEPTION WHEN OTHERS THEN ROLLBACK; RETURN err(SQLERRM);
  END admin_save_airport;

  -- בקרת שימוש: נפח אחסון (GB, סכמת ARKIA) ומיילים שנשלחו החודש — מול מכסות ה-Free Tier.
  FUNCTION admin_usage (p_token VARCHAR2) RETURN CLOB IS
    l_uid   NUMBER := admin_guard(p_token);
    l_gb    NUMBER := 0;
    l_mails NUMBER := 0;
  BEGIN
    -- שאילתות דינמיות (EXECUTE IMMEDIATE) כדי שהחבילה תתקמפל גם אם View/עמודה חסרים בסביבה
    BEGIN EXECUTE IMMEDIATE 'SELECT ROUND(NVL(SUM(bytes),0)/1024/1024/1024,3) FROM user_segments' INTO l_gb;
    EXCEPTION WHEN OTHERS THEN l_gb := 0; END;
    -- שם עמודת התאריך ב-APEX_MAIL_LOG משתנה בין גרסאות; מנסים כמה מועמדים ואז נופלים לספירה כוללת
    BEGIN
      DECLARE l_col VARCHAR2(30);
      BEGIN
        SELECT column_name INTO l_col FROM all_tab_columns
         WHERE table_name = 'APEX_MAIL_LOG' AND column_name IN ('LOG_TIMESTAMP','SENT_ON','CREATED_ON','AGGREGATE_ON')
         AND ROWNUM = 1;
        EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM apex_mail_log WHERE '||l_col||' >= TRUNC(SYSDATE,''MM'')' INTO l_mails;
      EXCEPTION WHEN OTHERS THEN
        BEGIN EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM apex_mail_log' INTO l_mails;
        EXCEPTION WHEN OTHERS THEN l_mails := 0; END;
      END;
    END;
    APEX_JSON.initialize_clob_output;
    APEX_JSON.open_object;
      APEX_JSON.write('ok', TRUE);
      APEX_JSON.write('storage_gb', l_gb);
      APEX_JSON.write('storage_limit_gb', 250);
      APEX_JSON.write('emails_month', l_mails);
      APEX_JSON.write('emails_limit', 3000);
    APEX_JSON.close_object;
    RETURN APEX_JSON.get_clob_output;
  EXCEPTION WHEN OTHERS THEN APEX_JSON.free_output; RETURN err(SQLERRM);
  END admin_usage;

