  -- עריכת פרטי הזמנה (יוזם / מאשר / אדמין) — מותרת רק עד לשלב הכרטוס
  FUNCTION edit_booking (p_token VARCHAR2, p_id NUMBER, p_departure VARCHAR2, p_agent_id NUMBER,
                         p_notes VARCHAR2, p_pax NUMBER, p_pref_time VARCHAR2, p_wet_op NUMBER,
                         p_origin VARCHAR2, p_dest VARCHAR2) RETURN CLOB IS
    l_uid NUMBER := uid(p_token);
    l_status VARCHAR2(30); l_dept NUMBER; l_ini NUMBER; l_apr NUMBER; l_is_dept_apr NUMBER; l_ok BOOLEAN;
  BEGIN
    SELECT status, dept_id, initiator_id, approver_id INTO l_status, l_dept, l_ini, l_apr
      FROM bookings WHERE booking_id = p_id;
    -- לא ניתן לערוך לאחר כרטוס / ביטול / דחייה
    IF l_status IN ('TICKETED','CANCELLED','REJECTED') THEN RETURN err('booking_locked'); END IF;
    SELECT COUNT(*) INTO l_is_dept_apr FROM dept_approvers WHERE dept_id=l_dept AND approver_user_id=l_uid;
    l_ok := (l_ini = l_uid)
            OR (has_role(l_uid,'ADMIN')='Y')
            OR (has_role(l_uid,'APPROVER')='Y' AND (l_apr = l_uid OR l_is_dept_apr > 0));
    IF NOT l_ok THEN RETURN err('forbidden'); END IF;
    UPDATE bookings
       SET departure_date  = NVL(TO_DATE(p_departure,'YYYY-MM-DD'), departure_date),
           agent_id        = NVL(p_agent_id, agent_id),
           quote_notes     = NVL(p_notes, quote_notes),
           pax_count       = NVL(p_pax, pax_count),
           pref_time       = p_pref_time,
           wet_operator_id = p_wet_op,
           origin_iata     = UPPER(p_origin),
           dest_iata       = UPPER(p_dest),
           updated_at      = SYSTIMESTAMP
     WHERE booking_id = p_id;
    -- שורת אודיט (מאותו סטטוס אל עצמו) — כדי שיירשם "עודכן לאחרונה ע"י"
    INSERT INTO booking_status_log (booking_id, from_status, to_status, action_by, note)
    VALUES (p_id, l_status, l_status, l_uid, 'עריכת פרטי הזמנה');
    COMMIT;
    RETURN '{"ok":true}';
  EXCEPTION WHEN OTHERS THEN ROLLBACK; RETURN err(SQLERRM);
  END edit_booking;

  FUNCTION do_action (p_token VARCHAR2, p_id NUMBER, p_action VARCHAR2,
                      p_price NUMBER, p_currency VARCHAR2, p_trip CLOB, p_notes VARCHAR2,
                      p_pnr VARCHAR2, p_ref VARCHAR2, p_tdate VARCHAR2, p_reason VARCHAR2,
                      p_low_cost VARCHAR2 DEFAULT NULL) RETURN CLOB IS
    l_uid NUMBER := uid(p_token);
  BEGIN
    CASE p_action
      WHEN 'submit_quote'   THEN
        -- מודל בריכה: הסוכן שמגיש את ההצעה משויך להזמנה (אם עדיין לא משויכת)
        UPDATE bookings SET agent_id = (SELECT MAX(agent_id) FROM agents WHERE user_id = l_uid)
         WHERE booking_id = p_id AND agent_id IS NULL;
        UPDATE bookings SET low_cost = NVL(p_low_cost,'N') WHERE booking_id = p_id;
        booking_pkg.submit_quote(p_id, p_price, p_currency, p_trip, p_notes, l_uid);
        notify_approver_quote(p_id);   -- מייל למאשר עם כפתור אישור ישיר
      WHEN 'approve'        THEN
        IF NOT is_booking_approver(l_uid, p_id) THEN RETURN err('forbidden'); END IF;
        booking_pkg.approve(p_id, l_uid, p_notes);
      WHEN 'reject'         THEN
        IF NOT is_booking_approver(l_uid, p_id) THEN RETURN err('forbidden'); END IF;
        booking_pkg.reject(p_id, l_uid, p_reason);
      WHEN 'ticket'         THEN booking_pkg.ticket(p_id, p_pnr, p_ref, TO_DATE(p_tdate,'YYYY-MM-DD'), l_uid);
      WHEN 'request_cancel' THEN
        -- אישור ביטול ע"י הסוכן נדרש רק לאחר כרטוס. לפני כרטוס — ביטול מיידי.
        DECLARE l_st VARCHAR2(30); BEGIN
          SELECT status INTO l_st FROM bookings WHERE booking_id = p_id;
          IF l_st = 'TICKETED' THEN
            booking_pkg.request_cancel(p_id, l_uid, p_reason);          -- דו-שלבי
          ELSE
            UPDATE bookings SET cancel_reason = p_reason, cancel_requested_by = l_uid,
                   cancel_requested_at = SYSTIMESTAMP WHERE booking_id = p_id;
            booking_pkg.change_status(p_id, 'CANCELLED', l_uid, p_reason);  -- מיידי
          END IF;
        END;
      WHEN 'confirm_cancel' THEN booking_pkg.confirm_cancel(p_id, l_uid, p_notes);
      ELSE RETURN err('unknown_action');
    END CASE;
    COMMIT;
    RETURN '{"ok":true}';
  EXCEPTION WHEN OTHERS THEN ROLLBACK; RETURN err(SQLERRM);
  END do_action;

  -- הצעת מחיר דחופה — הסוכן יוזם הצעה ללא בקשה. נפתחת ישר ב-QUOTE_RECEIVED,
