      END;
    END IF;
    -- אין מייל למשתמש
    RETURN '{"ok":true,"emailed":false,"username":"'||l_username||'","temp_password":"'||l_temp||'"}';
  EXCEPTION WHEN OTHERS THEN ROLLBACK; RETURN err(SQLERRM);
  END admin_reset_pw;

