  FUNCTION admin_reset_pw (p_token VARCHAR2, p_user_id NUMBER) RETURN CLOB IS
    l_uid NUMBER := admin_guard(p_token);
    l_username app_users.username%TYPE;
    l_email    app_users.email%TYPE;
